numero = input('digite algo ')
print('O tipo primitivo desse valor é', type(numero))
print('Só tem espaços?', numero.isspace())
print('é um numéro?', numero.isnumeric())
print ('é alfabético', numero.isalpha())
print('é alphanumerico?', numero.isalnum())
print('esta em maiúsculas?', numero.isupper())
print('está em minusculas?', numero.islower())
print ('está capitalizado?', numero.title())


